fn main() {
    // Maneira um
    let mut x = 4;
    println!("X is: {}", x);

    x = 5;
    println!("X is: {}", x);

    // Maneira dois

    let n = 4;
    println!("N is: {}", n);

    let n = 5;
    println!("N is: {}", n);

    // Exemplo 3
    let z = 4;
    println!("Z is: {}", z);

    let z = z + 3;
    println!("Z is: {}", z);
}
